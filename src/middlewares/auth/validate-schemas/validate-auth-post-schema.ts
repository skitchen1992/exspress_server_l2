import { checkSchema } from 'express-validator';

export const validateAuthPostSchema = () => {
  return checkSchema({
    loginOrEmail: {
      exists: {
        bail: true,
        errorMessage: 'Is required',
      },
      isString: {
        bail: true,
        errorMessage: 'Not a string',
      },
      trim: {},
    },
    password: {
      exists: {
        bail: true,
        errorMessage: 'Is required',
      },
      isString: {
        bail: true,
        errorMessage: 'Not a string',
      },
      trim: {},
    },
  });
};
